import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const principles = JSON.parse(
  await readFile("docs/principles/principles.json", "utf8")
);
const review = JSON.parse(
  await readFile("checklists/songstyle-review.json", "utf8")
);
const skillNames = [
  "songstyle-web-designer",
  "songstyle-design-reviewer"
];
const generatedComment = "<!-- Generated by scripts/build-skill-references.mjs. Do not edit manually. -->";

const principlesMarkdown = `${generatedComment}

# SongStyle Principles

${principles
  .map(
    (principle) => `## ${principle.nameZh} / ${principle.nameEn}

- ID: \`${principle.id}\`
- Summary: ${principle.summaryEn}
- Review question: ${principle.reviewQuestionEn}`
  )
  .join("\n\n")}
`;

const reviewMarkdown = `${generatedComment}

# SongStyle Review Model

Score each dimension from 0 to 4. Necessary density, usability, accessibility,
truthfulness, and business goals take priority over aesthetic preference.

${review.dimensions
  .map(
    (dimension) => `## ${dimension.nameZh} / ${dimension.nameEn}

- ID: \`${dimension.id}\`
- Question: ${dimension.question}
- Good: ${dimension.good}
- Risk: ${dimension.risk}`
  )
  .join("\n\n")}
`;

for (const skillName of skillNames) {
  const referenceDir = path.join("skills", skillName, "references");
  await mkdir(referenceDir, { recursive: true });
  await writeFile(path.join(referenceDir, "songstyle-principles.md"), principlesMarkdown);
  await writeFile(path.join(referenceDir, "review-model.md"), reviewMarkdown);
}
