import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const principles = JSON.parse(
  await readFile("docs/principles/principles.json", "utf8")
);
const review = JSON.parse(
  await readFile("checklists/songstyle-review.json", "utf8")
);
const executionGuide = await readFile("docs/guides/ui-ux-execution-model.md", "utf8");
const skillNames = [
  "songstyle-web-designer",
  "songstyle-design-reviewer"
];
const generatedComment = "<!-- Generated by scripts/build-skill-references.mjs. Do not edit manually. -->";

function renderList(label, items = []) {
  if (!items.length) return "";
  return `- ${label}:\n${items.map((item) => `  - ${item}`).join("\n")}`;
}

function renderScoreRubric(scoreRubric = {}) {
  const scores = ["0", "1", "2", "3", "4"];
  return `- Score rubric:\n${scores
    .map((score) => `  - ${score}: ${scoreRubric[score]}`)
    .join("\n")}`;
}

const principlesMarkdown = `${generatedComment}

# SongStyle Principles

${principles
  .map(
    (principle) => `## ${principle.nameZh} / ${principle.nameEn}

- ID: \`${principle.id}\`
- Summary zh: ${principle.summaryZh}
- Summary: ${principle.summaryEn}
- Review question zh: ${principle.reviewQuestionZh}
- Review question: ${principle.reviewQuestionEn}`
  )
  .join("\n\n")}
`;

const reviewMarkdown = `${generatedComment}

# SongStyle Review Model

Score each dimension from 0 to 4. Necessary density, usability, accessibility,
truthfulness, Chinese heading readability, and business goals take priority
over aesthetic preference. Review information through near, middle, and far
layers before scoring.

${review.dimensions
  .map(
    (dimension) => `## ${dimension.nameZh} / ${dimension.nameEn}

- ID: \`${dimension.id}\`
- Question: ${dimension.question}
- Good: ${dimension.good}
- Risk: ${dimension.risk}
${renderScoreRubric(dimension.scoreRubric)}
${renderList("Critical failures", dimension.criticalFailures)}
${renderList("Evidence required", dimension.evidenceRequired)}`
  )
  .join("\n\n")}
`;

const executionMarkdown = `${generatedComment}

${executionGuide.replace(/^---\n[\s\S]*?\n---\n+/, "")}`;

for (const skillName of skillNames) {
  const referenceDir = path.join("skills", skillName, "references");
  await mkdir(referenceDir, { recursive: true });
  await writeFile(path.join(referenceDir, "songstyle-principles.md"), principlesMarkdown);
  await writeFile(path.join(referenceDir, "review-model.md"), reviewMarkdown);
  await writeFile(path.join(referenceDir, "ui-ux-execution-model.md"), executionMarkdown);
}
